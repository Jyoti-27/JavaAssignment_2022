// Using for  and nested for statement



package assignment1_2022;

public class Practice_Program_8 {

	public static void main(String[] args) {
		
		int n=5;
		for(int i=n;i>=0;i--) 
		{
			for(int j=0;j<=i;j++) 
			{
				System.out.print(" * ");
			}
			System.out.println( );

	}

  }
}


